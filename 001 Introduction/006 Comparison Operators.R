# Comparison Operators

a <- 5
b <- 10

print(a == b)

print(a != b)

print(a > b)

print(a < b)

print(a <= b)

print(a >= b)

# Vector
## Returns vector of boolean values
vector1 <- c(1,2,3,4,5)
vector1 < 2


## Vector by Vector Comparison
vector2 <- c(10,20,30,40,50)

vector2 > vector1
vector2 < vector1