# Vector Operations

vec1 <- c(1,2,3)
vec2 <- c(4,5,6)

## Combine two vectors
vec3 <- c(vec1, vec2)
print(vec3)

## Addition
print(vec1 + vec2)

## Substraction
print(vec1 - vec2)

## Mutliply
print(vec1 * vec2)

# Built In Vectors
## 1. Sum
sum(vec1)

## 2. Mean
mean(vec1)

## 3. Standard Deviation
sd(vec1)

## 4. Minimum
min(vec1)

## 4. Maximum
max(vec1)

## 5. Prod
prod(vec1)